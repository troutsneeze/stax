#include <stdio.h>
#include <allegro.h>

#include "stax.h"

#define MOVE_DURATION (TIMER_FREQ/8)
#define KILL_DURATION (TIMER_FREQ/4)

#define NUM_LEVELS  31

struct Puzzle_Level {
	int turns;
	char blocks[PANEL_H][PANEL_W];
};

/* see below for the actual level definitions */
extern struct Puzzle_Level Levels[NUM_LEVELS];

char *messages[3] = {
	"Hey, you're doing pretty good!",
	"Don't you have something IMPORTANT to do?",
	"Congradulations, you win! No really, that's it!"
};

BITMAP *bg;
BITMAP *cursor;
SAMPLE *start;
SAMPLE *blast;
SAMPLE *lose;

void puzzle_loop(void)
{
	int x, y;
	int level = 0, turns, blocks_remaining;
	Panel *p;
	Block *b;
	Theme *t;
	Input i;
	int n, timer_tick = 0, quit = 0, retry = 0, done_moving = 0;
	int cursor_x, cursor_y;
	int move_count, kill_count;
	int time, retries = 0;

	time = 0;

	t = calloc(1, sizeof(Theme));

	/* fake a full theme */
	for (n = 0; n < NUM_BLOCKS; n++) {
		t->blocks[n] = find_object_data(dat, "PUZZLE_BLOCK%d", n);
		t->blocks_h[n] = find_object_data(dat, "PUZZLE_BLOCK%d_H", n);
	}
	t->collide = find_object_data(dat, "PUZZLE_COLLIDE");
	t->pop = find_object_data(dat, "PUZZLE_POP");

	bg = find_object_data(dat, "PUZZLE_BG");
	cursor = find_object_data(dat, "PUZZLE_CURSOR");
	start = find_object_data(dat, "PUZZLE_START");
	blast = find_object_data(dat, "PUZZLE_BLAST");
	lose = find_object_data(dat, "PUZZLE_LOSE");

	play_sample(start, 255, 128, 1000, 0);
	zoom_image(bg);

	cursor_x = PANEL_W/2;
	cursor_y = PANEL_H-1;

nextlevel:
	turns = Levels[level].turns;
	blocks_remaining = 0;

	p = calloc(1, sizeof(Panel));
	if (!p) {
		error("Out of memory!", NULL, NULL);
		free(t);
		return;
	}

	p->bmp = create_sub_bitmap(vs,10,20,PANEL_W*BLOCK_SIZE,PANEL_H*BLOCK_SIZE);
	if (!p->bmp) {
		error("Out of memory!", NULL, NULL);
		free(t);
		free(p);
		return;
	}

	p->x = 10;
	p->y = 20;

	for (y = 0; y < PANEL_H; y++)
		for (x = 0; x < PANEL_W; x++) {
			if (Levels[level].blocks[y][x]) {
				blocks_remaining++;
				block_add(p->blocks, x, itofix(y*BLOCK_SIZE), Levels[level].blocks[y][x]);
			}
			if (!y)
				block_add(p->blocks, x, itofix(PANEL_H*BLOCK_SIZE), 1);
		}
	
	p->rise_speed = 0;
	p->fall_speed = (level == NUM_LEVELS-1) ? ftofix(0.01) : ftofix(0.1);

	move_count = MOVE_DURATION;
	kill_count = KILL_DURATION;

	while (!quit) {
		tick = 0;
		blit(bg, vs, 0, 0, 0, 0, bg->w, bg->h);
		draw_panel(p, t);
		draw_sprite(vs, cursor, cursor_x*BLOCK_SIZE+10, cursor_y*BLOCK_SIZE+20);
		x = time / TIMER_FREQ / 60;
		y = (time - (x * TIMER_FREQ * 60)) / TIMER_FREQ;
		text_mode(-1);
		textprintf_shadow(vs, font, 225, 80, C_BCYAN, C_CYAN,  "%2d:%02d", x, y);
		textprintf_shadow(vs, font, 220, 90, C_BPURPLE, C_PURPLE, "Level");
		textprintf_shadow(vs, font, 255, 90, C_BPURPLE, C_PURPLE, "%d", level+1);
		textprintf_shadow(vs, font, 220, 100, C_GREEN, C_DGREEN, "Turns");
		textprintf_shadow(vs, font, 255, 100, C_GREEN, C_DGREEN, "%d", turns);
		textprintf_shadow(vs, font, 220, 110, C_YELLOW, C_DYELLOW, "Retry");
		textprintf_shadow(vs, font, 255, 110, C_YELLOW, C_DYELLOW, "%d", retries);
		blit(vs, screen, 0, 0, 0, 0, vs->w, vs->h);
		poll_keyboard();
		if (key[KEY_ESC]) {
			/* hide stuff so people don't cheat */
			blit(bg, screen, 0, 0, 0, 0, bg->w, bg->h);
			quit = !prompt("Really Quit?", "Yes", "No");
			continue;
		}
		else if (key[KEY_P]) {
			blit(bg, screen, 0, 0, 0, 0, bg->w, bg->h);
			n = tick;
			do_pause();
			tick = n;
		}
		else if (key[KEY_BACKSPACE])
			screenshot();
		else if (key[KEY_EQUALS]) {
			if (level+1 < NUM_LEVELS) {
				rest(100);
				level++;
				quit = retry = 1;
				continue;
			}
		}
		done_moving = !move_blocks(p, t, timer_tick);
		get_input(&i, cfg.input1, timer_tick);
		move_count -= timer_tick;
		if (move_count < 0) move_count = 0;
		kill_count -= timer_tick;
		if (kill_count < 0) kill_count = 0;
		if (i.left && !move_count && cursor_x) {
			cursor_x--;
			move_count = MOVE_DURATION;
		}
		else if (i.right && !move_count && cursor_x < PANEL_W-1) {
			cursor_x++;
			move_count = MOVE_DURATION;
		}
		else if (i.up && !move_count && cursor_y) {
			cursor_y--;
			move_count = MOVE_DURATION;
		}
		else if (i.down && !move_count && cursor_y < PANEL_H-1) {
			cursor_y++;
			move_count = MOVE_DURATION;
		}
		if (i.button && !kill_count && done_moving) {
			for (b = p->blocks[cursor_x]; b; b = b->next)
				if ((b->y>>16)/BLOCK_SIZE == cursor_y) {
					play_sample(blast, 255, 128, 1000, 0);
					block_delete(p->blocks, b);
					blocks_remaining--;
					turns--;
					done_moving = 0;
				}
			kill_count = KILL_DURATION;
		}
		blocks_remaining -= check_blocks(p);
		if (done_moving && !blocks_remaining) {
			char *msg;
			fixed xpos;
			x = time / 1000 / 60;
			y = (time - (x * 1000 * 60)) / 1000;
			if (level++ < NUM_LEVELS-1) {
				n = tick;
				if (level % 10 == 0)
					msg = messages[level/10-1];
				else
					msg = "Level complete...";
				blit(bg, screen, 0, 0, 0, 0, bg->w, bg->h);
				xpos = itofix(-text_length(font, msg));
				clear_keybuf();
				while (xpos < itofix(SCREEN_W+text_length(font, msg))) {
					tick = 0;
					poll_joystick();
					if (keypressed() || joy_b1 || joy_b2)
						break;
					blit(bg, vs, 0, 95, 0, 95, bg->w, 10);
					textprintf_shadow(vs, font, xpos>>16, 95, C_WHITE, C_BLACK, msg);
					blit(vs, screen, 0, 95, 0, 95, vs->w, 10);
					xpos += fixmul(ftofix(0.1), itofix(tick));
				}
				clear_keybuf();
				tick = n;
				retry = 1;
			}
			quit = 1;
		}
		else if (done_moving && !turns) {
			play_sample(lose, 255, 128, 1000, 0);
			rest(1000);
			n = tick;
			retry = !prompt("Try again?", "Yes", "No");
			if (retry)
				retries++;
			tick = n;
			quit = 1;
			done_moving = 0;
		}
		time += tick;
		timer_tick = tick;
	}

	free_panel(p);
	
	if (retry) {
		retry = quit = 0;
		goto nextlevel;
	}
	
	free_theme(t);
	clear_keybuf();
}

